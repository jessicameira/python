pizzas = ['calabreza', 'pepperoni', 'mussarela']
mensagem = 'Eu gosto de '

for pizza in pizzas:
    print(mensagem + pizza)
    
print("\nEu realmente adoro pizza!\n")

animais = ['gato', 'cachorro', 'hamster']

for animal in animais:
    print("Um " + animal + " seria um otimo animal de estimação!")
    
print("\nQualquer um desses seria um ótimo animal de estimação.")
